package calcIt;

public class multiplication {
	static int multInt(int x, int y) {
		int z = x * y;
		return z; 
	}
	
	static float multFloat(float x, float y) {
		float z = x * y;
		return z;
	}
	
	static double multDoub(double x, double y) {
		double z = x * y;
		return z;
	}
	
	static long multlong(long x, long y) {
		long z = x * y;
		return z;
	}
}
