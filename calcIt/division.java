package calcIt;

public class division {
	static int diviInt(int x, int y) {
		int z = x / y;
		return z; 
	}
	
	static float diviFloat(float x, float y) {
		float z = x / y;
		return z;
	}
	
	static double diviDoub(double x, double y) {
		double z = x / y;
		return z;
	}
	
	static long divilong(long x, long y) {
		long z = x / y;
		return z;
	}

}
