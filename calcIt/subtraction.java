package calcIt;

public class subtraction {
	static int subInt(int x, int y) {
		int z = x - y;
		return z; 
	}
	
	static float subFloat(float x, float y) {
		float z = x - y;
		return z;
	}
	
	static double subDoub(double x, double y) {
		double z = x - y;
		return z;
	}
	
	static long sublong(long x, long y) {
		long z = x - y;
		return z;
	}
}
