package calcIt;

public class addition {
	static int addInt(int x, int y) {
		int z = x + y;
		return z; 
	}
	
	static float addFloat(float x, float y) {
		float z = x + y;
		return z;
	}
	
	static double addDoub(double x, double y) {
		double z = x + y;
		return z;
	}
	
	static long addlong(long x, long y) {
		long z = x + y;
		return z;
	}
	
}
